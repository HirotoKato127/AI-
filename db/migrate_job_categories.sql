-- ============================================================
-- 1. 職種マスターテーブル
-- ============================================================
CREATE TABLE IF NOT EXISTS job_category_master (
  id          SERIAL PRIMARY KEY,
  major_category TEXT NOT NULL,         -- 大分類名
  sub_category   TEXT NOT NULL,         -- 小分類名
  sort_order     INTEGER DEFAULT 0,     -- 表示順
  is_active      BOOLEAN DEFAULT TRUE,
  created_at     TIMESTAMPTZ DEFAULT NOW(),
  updated_at     TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(major_category, sub_category)
);

-- ============================================================
-- 2. 企業×職種 中間テーブル
-- ============================================================
CREATE TABLE IF NOT EXISTS client_job_categories (
  id              SERIAL PRIMARY KEY,
  client_id       BIGINT  NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  job_category_id INTEGER NOT NULL REFERENCES job_category_master(id),
  created_at      TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(client_id, job_category_id)
);

CREATE INDEX IF NOT EXISTS idx_cjc_client   ON client_job_categories(client_id);
CREATE INDEX IF NOT EXISTS idx_cjc_category ON client_job_categories(job_category_id);

